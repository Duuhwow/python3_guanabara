# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

n1 = int(input('Digite um número: '))

dobro = n1 * 2
triplo = n1 * 3
raiz = n1 ** 0.5

print(f'O dobro de {n1} é {dobro}, o triplo é {triplo} e a raiz quadrada é {raiz:.0f}')

